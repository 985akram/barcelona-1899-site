window.onload = function() {
    const joinBtn = document.getElementById("join-btn");
    const joinForm = document.getElementById("join-form");
    const welcomeMessage = document.getElementById("welcome-message");

    // إخفاء زر "انضم إلينا" وعرض النموذج
    joinBtn.addEventListener("click", function() {
        joinForm.style.display = "block";  // إظهار نموذج التسجيل
        joinBtn.style.display = "none";   // إخفاء الزر
    });

    // عند إرسال النموذج
    joinForm.addEventListener("submit", function(event) {
        event.preventDefault();  // منع إعادة تحميل الصفحة عند إرسال النموذج

        // الحصول على القيم المدخلة
        const fullName = document.getElementById("full-name").value;
        const email = document.getElementById("email").value;

        // التحقق من أن الحقول غير فارغة
        if (fullName && email) {
            // عرض رسالة الترحيب مع الاسم الكامل
            welcomeMessage.innerHTML = `مرحبًا، ${fullName}! شكراً لانضمامك إلينا.`;
            welcomeMessage.style.display = "block";

            // إخفاء نموذج التسجيل
            joinForm.style.display = "none";
        } else {
            alert("الرجاء إدخال كافة الحقول.");
        }
    });
};
