// دالة لتخزين حالة الاشتراك في الكوكيز
function setSubscriptionCookie() {
    document.cookie = "subscribed=true; path=/; max-age=" + (60 * 60 * 24 * 365); // الكوكيز لمدة سنة
}

// دالة للتحقق إذا كان المستخدم قد اشترك
function checkSubscriptionStatus() {
    let cookies = document.cookie.split(';');
    for (let i = 0; i < cookies.length; i++) {
        let cookie = cookies[i].trim();
        if (cookie === "subscribed=true") {
            return true; // تم الاشتراك
        }
    }
    return false; // لم يتم الاشتراك
}

// إخفاء صفحة الانضمام إذا تم الاشتراك
